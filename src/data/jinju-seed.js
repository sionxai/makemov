// ==========================================
// 시나리오 → Markdown 변환
// ==========================================

export function screenplayToMarkdown(scenes) {
    if (!scenes || scenes.length === 0) return '';
    let md = '═══════════════════════════════════════\n';
    md += '         시나리오 (Screenplay)\n';
    md += '═══════════════════════════════════════\n\n';
    scenes.forEach((s) => {
        md += `━━━ Scene ${s.number} ━━━\n`;
        md += `${s.heading}\n\n`;
        if (s.action) md += `[액션]\n${s.action}\n\n`;
        if (s.dialogue) md += `[대사]\n${s.dialogue}\n\n`;
        if (s.notes) md += `[연출 노트]\n${s.notes}\n\n`;
    });
    return md;
}
